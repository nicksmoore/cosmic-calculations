export const getHouseInterpretation = (
  houseNumber: number,
  signName: string,
  planetNames: string[]
): { signInterpretation: string; planetInterpretations: { name: string; interpretation: string }[] } => {
  const house = houseInterpretations[houseNumber];
  if (!house) {
    return { signInterpretation: "", planetInterpretations: [] };
  }

  const signInterpretation = house.signMeaning[signName] || "";
  
  const planetInterpretations = planetNames.map((name) => ({
    name,
    interpretation: house.planetMeaning[name] || "",
  })).filter(p => p.interpretation);

  return { signInterpretation, planetInterpretations };
};
